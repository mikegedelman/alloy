// straight from https://wiki.osdev.org/IDT
struct idt_entry {
   uint16_t offset_1; // offset bits 0..15
   uint16_t selector; // a code segment selector in GDT or LDT
   uint8_t zero;      // unused, set to 0
   uint8_t type_attr; // type and attributes, see below
   uint16_t offset_2; // offset bits 16..31
};


struct idt_ptr
{
    uint16_t limit;
    uint32_t base;
} __attribute__((packed));

struct idt_entry idt[256];
struct idt_ptr idtp;

/*
To generrate:

for i in {0..255}
do cat <<EOF
fn = (uint32_t) int$i;
idt[$i].offset_1 = (uint16_t) (fn & 0xFFFF);
idt[$i].selector = 0x08;
idt[$i].zero = 0;
idt[$i].type_attr = 0x8E;
idt[$i].offset_2 = (uint16_t) (fn >> 16);
EOF
done
*/
